package model;

public class Car {
    private int id;
    private String brand;
    private String plateNumber;
    private String color;
    private int year;

    public Car(int id, String brand, String plateNumber, String color, int year) {
        this.id = id;
        this.brand = brand;
        this.plateNumber = plateNumber;
        this.color = color;
        this.year = year;
    }

}
